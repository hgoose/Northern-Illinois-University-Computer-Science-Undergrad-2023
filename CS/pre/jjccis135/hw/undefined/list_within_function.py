#!/usr/bin/env python3

def printList(list_to_print):
    """ Function Takes in list and iterates over list to display values """
    index = 0
    while index < len(list_to_print):
         print(list_to_print[index])
         index += 1

py_list = [1, 2, 4, 8, 16, 32, 64, 128, 256, 512]

printList(py_list)



















