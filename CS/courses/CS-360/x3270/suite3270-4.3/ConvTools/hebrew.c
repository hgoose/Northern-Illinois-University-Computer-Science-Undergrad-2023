int
main(int argc, char *argv[])
{
	int i;
	unsigned char ebc2cg[256];
	unsigned short cg2u[256];

	/* Use hebrew_remap to rearrange ebc2cg0 into a Hebrew ebc2cg. */
	/*memcpy(ebc2cg, ebc2cg0, sizeof(ebc2cg));*/
	memset(ebc2cg, '\0', sizeof(ebc2cg));
	for (i = 0; i < 256; i++) {
	    if (hebrew_remap[i] != 0)
		ebc2cg[i] = asc2cg[hebrew_remap[i]];
	}

	/* Now create cg2u from ebc2cg and cp424. */
	memset(cg2u, '\0', sizeof(cg2u));
	for (i = 0x41; i <= 0xfe; i++) {
	    if ((cp424[i-0x41] & 0xff00) == 0x0500)
		cg2u[ebc2cg[i]] = cp424[i-0x41];
	}

	/* Dump cg2u out. */
	for (i = 0; i < 256; i++) {
	    if (i && !(i % 4))
		printf("\n");
	    if (!(i % 4))
		printf("\t /* CG %03x */ ", i);
	    printf("0x%08x, ", cg2u[i]);
	}
	printf("\n");
	return 0;
}

