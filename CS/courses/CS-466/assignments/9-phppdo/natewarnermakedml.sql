CREATE TABLE `S` (
  `S` char(2) NOT NULL,
  `SNAME` char(10) NOT NULL,
  `STATUS` int(11) NOT NULL,
  `CITY` char(12) NOT NULL,
  PRIMARY KEY (`S`) 
);

CREATE TABLE `P` (
  `P` char(2) NOT NULL,
  `PNAME` char(10) NOT NULL,
  `COLOR` char(10) NOT NULL,
  `WEIGHT` int(11) NOT NULL,
  PRIMARY KEY (`P`)
);

CREATE TABLE `SP` (
  `S` char(2) NOT NULL,
  `P` char(2) NOT NULL,
  `QTY` int(11) NOT NULL,
  PRIMARY KEY (`S`,`P`),
  FOREIGN KEY (`S`) REFERENCES `S` (`S`),
  FOREIGN KEY (`P`) REFERENCES `P` (`P`)
);

